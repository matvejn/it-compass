def get_unique_item(user_id, item_list, seen_key):
    """Возвращает уникальный элемент, который пользователь еще не видел"""
    data = load_data()
    if user_id not in data['seen_data']:
        data['seen_data'][user_id] = {'seen_facts': [], 'seen_tips': [], 'seen_games': []}
    
    seen = data['seen_data'][user_id].get(seen_key, [])
    
    # Если пользователь видел все элементы, сбрасываем историю
    if len(seen) >= len(item_list):
        seen = seen[-len(item_list)//2:]
    
    # Находим элементы, которые пользователь не видел
    available_indices = [i for i in range(len(item_list)) if i not in seen]
    
    if available_indices:
        idx = random.choice(available_indices)
    else:
        idx = random.randint(0, len(item_list) - 1)
    
    seen.append(idx)
    data['seen_data'][user_id][seen_key] = seen
    save_data(data)
    return item_list[idx]

