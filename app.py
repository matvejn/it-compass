DATA_FILE = 'user_data.json'

def load_data():
    """Загружает данные пользователей из файла"""
    if os.path.exists(DATA_FILE):
        try:
            with open(DATA_FILE, 'r', encoding='utf-8') as f:
                return json.load(f)
        except:
            return {'user_stats': {}, 'user_progress': {}, 'seen_data': {}}
    return {'user_stats': {}, 'user_progress': {}, 'seen_data': {}}

def save_data(data):
    """Сохраняет данные пользователей в файл"""
    with open(DATA_FILE, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

def get_user_data(user_id):
    """Получает данные конкретного пользователя"""
    data = load_data()
    if user_id not in data['user_stats']:
        data['user_stats'][user_id] = {
            'tests_completed': 0,
            'junior_tests': 0,
            'senior_tests': 0,
            'games_played': 0,
            'first_visit': datetime.now().strftime('%d.%m.%Y')
        }
    if user_id not in data['user_progress']:
        data['user_progress'][user_id] = {
            'creativity': 0,
            'logic': 0,
            'teamwork': 0,
            'attention': 0,
            'patience': 0,
            'history': []
        }
    if user_id not in data['seen_data']:
        data['seen_data'][user_id] = {
            'seen_facts': [],
            'seen_tips': [],
            'seen_games': []
        }
    save_data(data)
    return data

def update_user_data(user_id, stats=None, progress=None, seen=None):
    """Обновляет данные пользователя"""
    data = load_data()
    if stats:
        data['user_stats'][user_id] = stats
    if progress:
        data['user_progress'][user_id] = progress
    if seen:
        data['seen_data'][user_id] = seen
    save_data(data)

