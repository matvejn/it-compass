def game():
    """Игра - страница с вопросом"""
    if 'user_id' not in session:
        session['user_id'] = secrets.token_hex(8)
    
    game_data = get_unique_game(session['user_id']).copy()
    
    # Создаем список вариантов с индексами
    options_with_indices = list(enumerate(game_data['options']))
    
    # Перемешиваем варианты
    random.shuffle(options_with_indices)
    
    # Находим, где теперь находится правильный ответ
    correct_value = game_data['correct']
    new_correct_index = None
    
    for new_idx, (old_idx, option) in enumerate(options_with_indices):
        if old_idx == correct_value:
            new_correct_index = new_idx
            break
    
    # Сохраняем данные для обработки ответа
    session['current_game'] = {
        'correct': new_correct_index,
        'explanation': game_data['explanation'],
        'options': [opt for _, opt in options_with_indices],
        'original_options': game_data['options']
    }
    
    return render_template('game.html', 
                          description=game_data['description'],
                          options=[opt for _, opt in options_with_indices])

