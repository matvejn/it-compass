    if not answers:
        return redirect(url_for('index'))
    
    if 'user_id' not in session:
        session['user_id'] = secrets.token_hex(8)
    
    # Анализируем результаты
    scores = analyze_junior_scores(answers)
    profession_key = max(scores, key=scores.get) if scores else 'programmer'
    
    # Получаем описание профессии
    profession_description = get_junior_profession_description(profession_key)
    
    # Получаем ТОП-3 профессии
    top_professions = get_top_professions(scores, "junior")
    
    # Обновляем статистику
    user_id = session['user_id']
    data = load_data()
    if user_id not in data['user_stats']:
        data['user_stats'][user_id] = {
            'tests_completed': 0,
            'junior_tests': 0,
            'senior_tests': 0,
            'games_played': 0,
            'first_visit': datetime.now().strftime('%d.%m.%Y')
        }
    
    data['user_stats'][user_id]['tests_completed'] = data['user_stats'][user_id].get('tests_completed', 0) + 1
    data['user_stats'][user_id]['junior_tests'] = data['user_stats'][user_id].get('junior_tests', 0) + 1
    
    # Обновляем прогресс
    update_user_progress(user_id, "junior", answers)
    
    # Получаем советы и ресурсы
    advice = get_junior_advice()
    resources = get_learning_resources(profession_key)
    
    return render_template('junior_result.html',
                          profession_description=profession_description,
                          top_professions=top_professions,
                          advice=advice,
                          resources=resources)

